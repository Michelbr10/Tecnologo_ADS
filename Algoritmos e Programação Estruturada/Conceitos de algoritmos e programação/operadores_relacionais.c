#include <stdio.h>

int main()
{
    //iniciando variáveis e atribuindo valores
    int a = 5, b = 10;
    //printando as comparações booleanas
    printf("a e b sao iguais: %d \n", a == b); //a == b false
    printf("a e b sao diferentes: %d \n", a != b); //a != b true
    printf("a e maior que b: %d \n", a > b); //a > b false
    printf("a e menor que b: %d \n", a < b); //a < b true

    return 0;
}